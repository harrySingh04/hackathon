package com.start.Hackathon.model.events;

public class PointCoordinate {
	
	String x;
	String y;
	
	
	public PointCoordinate(String x, String y) {
		super();
		this.x = x;
		this.y = y;
	}
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	public String getY() {
		return y;
	}
	public void setY(String y) {
		this.y = y;
	}
	
	

}
